from math import *

# working with numbers
print(2 * 4 + 5)

# convert to a string
my_num = 5
print(str(my_num))
# in Javascript this will be my_num.toString() or String(my_num)

negative_num = -3
print(abs(negative_num))
# gives absolute value of negative number

print(pow(3, 2))
# in Javascript this will be Math.pow(3, 2)

print(max(5, 6))
# in Javascript this will be Math.max(5, 6)

print(min(5, 6))
# in Javascript this will be Math.min(5, 6)

print(round(2.53))
# in Javascript this will be Math.round(2.53)

print(floor(3.26))
# in Javascript this will be Math.floor(3.26)

print(ceil(4.65))
# in Javascript this will be Math.ceil(4.65)

print(sqrt(256))
# in Javascript this will be Math.sqrt(256)
